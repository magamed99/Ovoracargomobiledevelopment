/**
 * Список стран для нормализации адресов
 */
const COUNTRIES = [
  'Таджикистан', 'Россия', 'Узбекистан', 'Казахстан',
  'Кыргызстан', 'Туркменистан', 'Китай', 'Афганистан',
];

/**
 * Очищает адрес — оставляет только город/район/область.
 * Убирает названия стран и лишние части из полного адреса.
 */
export function cleanAddress(address: string): string {
  if (!address) return address;
  if (COUNTRIES.includes(address.trim())) return address;
  const parts = address.split(',').map(p => p.trim());
  const filtered = parts.filter(part => !COUNTRIES.includes(part));
  return filtered[0] || address;
}

/**
 * Определяет страну по названию города
 */
export function getCityCountry(cityName: string): string {
  const name = cityName?.toLowerCase().trim() || '';

  const TAJIKISTAN = ['душанбе','худжанд','куляб','курган-тюбе','истаравшан','турсунзаде','канибадам','вахдат','рогун','пенджикент','хорог','нурек','гиссар','бохтар'];
  const RUSSIA = ['москва','санкт-петербург','новосибирск','екатеринбург','казань','нижний новгород','челябинск','самара','омск','ростов-на-дону','уфа','красноярск','воронеж','пермь','волгоград','краснодар','саратов','тюмень','тольятти','ижевск','барнаул','иркутск'];
  const KAZAKHSTAN = ['алматы','астана','шымкент','караганда','актобе','тараз','павлодар','усть-каменогорск','семей','атырау','костанай','кызылорда','уральск','петропавловск','актау'];
  const KYRGYZSTAN = ['бишкек','ош','джалал-абад','каракол','токмок','узген','балыкчы','кара-балта','талас','нарын','баткен','кызыл-кия','кара-суу','таш-кумыр','майлуу-суу'];
  const UZBEKISTAN = ['ташкент','самарканд','бухара','андижан','наманган','фергана','нукус','карши','коканд','маргилан','гулистан','джизак','термез','навои','ургенч'];
  const CHINA = ['урумчи','пекин','шанхай','гуанчжоу','шэньчжэнь','кашгар','харбин','чэнду','сиань','ухань','чунцин','тяньцзинь','нанкин','ханчжоу','далянь'];
  const AFGHANISTAN = ['кабул','герат','кандагар','мазари-шариф','джелалабад','кундуз','балх','файзабад','талокан','баглан'];

  if (TAJIKISTAN.includes(name)) return 'Таджикистан';
  if (RUSSIA.includes(name)) return 'Россия';
  if (KAZAKHSTAN.includes(name)) return 'Казахстан';
  if (KYRGYZSTAN.includes(name)) return 'Кыргызстан';
  if (UZBEKISTAN.includes(name)) return 'Узбекистан';
  if (CHINA.includes(name)) return 'Китай';
  if (AFGHANISTAN.includes(name)) return 'Афганистан';
  return 'Таджикистан';
}

/**
 * Флаг страны
 */
export function countryFlag(country: string): string {
  const flags: Record<string, string> = {
    'Таджикистан': '🇹🇯',
    'Россия': '🇷🇺',
    'Казахстан': '🇰🇿',
    'Кыргызстан': '🇰🇬',
    'Узбекистан': '🇺🇿',
    'Китай': '🇨🇳',
    'Афганистан': '🇦🇫',
    'Туркменистан': '🇹🇲',
  };
  return flags[country] || '🌍';
}
